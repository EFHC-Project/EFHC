"""
==============================================================================
== EFHC Bot — states
------------------------------------------------------------------------------
Назначение: описать FSM состояния для пользовательских сценариев бота (отправка
доказательств по заданиям и т.п.), чтобы логика aiogram оставалась детерминирована.

Канон/инварианты:
  • Балансы не изменяет; только описывает состояния FSM.
  • Названия состояний стабильны и не влияют на бизнес-логику банка.

ИИ-защиты/самовосстановление:
  • Чёткие состояния уменьшают вероятность «залипания» пользователей между
    шагами и упрощают рестарты бота.

Запреты:
  • Не добавляет сетевых вызовов и не трогает БД.
  • Не хранит конфигурацию VIP/NFT — только FSM-этапы.
==============================================================================
"""

from __future__ import annotations

from aiogram.fsm.state import State, StatesGroup


class TaskSubmissionStates(StatesGroup):
    """Состояния отправки доказательств по заданиям."""

    waiting_for_proof: State = State()
    confirmation: State = State()
