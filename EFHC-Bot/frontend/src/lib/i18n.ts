/**
======================================================================
== EFHC Bot WebApp — минимальный i18n                                 ==
======================================================================
Назначение: предоставляет простую функцию перевода по словарям без влияния на бизнес-логику.
Инварианты: не меняет деньги и kWh; на клиенте только отображение строк.
ИИ-защита: fallback на ключ, если перевод отсутствует, чтобы UI оставался читабельным.
Запреты: не подгружает внешние скрипты, не выполняет сетевых запросов при рендере.
======================================================================
*/
type Dictionary = Record<string, string>;

type Locale = "ru" | "en" | "ua" | "de" | "fr" | "es" | "it" | "pl";

const dictionaries: Partial<Record<Locale, Dictionary>> = {
  en: {
    energy: "Energy",
    panels: "Panels",
    exchange: "Exchange",
    shop: "Shop",
    rating: "Rating",
    referrals: "Referrals",
    tasks: "Tasks",
    ads: "Ads",
    admin: "Admin",
  },
  ru: {
    energy: "Энергия",
    panels: "Панели",
    exchange: "Обмен",
    shop: "Магазин",
    rating: "Рейтинг",
    referrals: "Рефералы",
    tasks: "Задания",
    ads: "Реклама",
    admin: "Админ",
  },
};

export function getTranslator(locale: Locale = "ru") {
  const dict = dictionaries[locale] ?? dictionaries.ru ?? {};
  return (key: string): string => dict[key] ?? key;
}

