/**
======================================================================
== EFHC Bot WebApp — обмен kWh → EFHC                                 ==
======================================================================
Назначение: показывает возможность обмена kWh в EFHC 1:1, без обратной конверсии.
Инварианты: курс 1 EFHC = 1 kWh; пользователь никогда не уходит в минус — проверяется на сервере.
ИИ-защита: UI не выполняет операцию сам, лишь отображает правила; никакие POST здесь не вызываются.
Запреты: нет EFHC→kWh, нет P2P переводов.
======================================================================
*/
type Props = {
  availableKwh: number;
};

function ExchangePanel({ availableKwh }: Props) {
  return (
    <section>
      <h2>Обмен kWh → EFHC</h2>
      <p>Доступно к обмену: {availableKwh.toFixed(8)} kWh</p>
      <p>Курс фиксирован: 1 kWh = 1 EFHC (только в эту сторону).</p>
      <p>
        Для обмена используйте раздел Exchange в приложении — сервер требует
        Idempotency-Key и не позволит отрицательный баланс.
      </p>
    </section>
  );
}

export default ExchangePanel;

