/**
======================================================================
== EFHC Bot WebApp — рекламный баннер                                 ==
======================================================================
Назначение: выводит рекламные кампании без начислений и списаний средств.
Инварианты: все показатели и ссылки приходят из бэкенда; клиент только показывает CTA.
ИИ-защита: при пустом списке безопасно сообщает об отсутствии кампаний.
Запреты: не кликает и не считает показы/клики локально, не списывает EFHC.
======================================================================
*/
type AdsBannerProps = {
  id: string;
  title: string;
  cta: string;
};

function AdsBanner({ id, title, cta }: AdsBannerProps) {
  return (
    <section>
      <h2>Реклама</h2>
      <div id={`ads-${id}`}>
        <strong>{title}</strong>
        <p>{cta}</p>
      </div>
    </section>
  );
}

export default AdsBanner;

// ======================================================================
// Пояснения «для чайника»:
//   • Баннер только отображает данные кампании.
//   • Подсчёт показов/кликов и оплата происходят на сервере.
//   • На клиенте нет денежных операций и никакой P2P логики.
// ======================================================================
