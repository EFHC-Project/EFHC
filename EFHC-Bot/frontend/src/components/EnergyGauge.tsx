/**
======================================================================
== EFHC Bot WebApp — виджет энергии                                  ==
======================================================================
Назначение: показывает доступные kWh, суммарную генерацию и VIP-статус без перерасчёта на клиенте.
Инварианты: числа приходят из бэкенда, клиент их не меняет; запрет EFHC→kWh соблюдается (нет обратных действий).
ИИ-защита: безопасный рендер при нулях и undefined, без падений UI.
Запреты: не инициирует обмен, не списывает/не начисляет EFHC.
======================================================================
*/
type Props = {
  availableKwh: number;
  totalGeneratedKwh: number;
  isVip: boolean;
};

function EnergyGauge({ availableKwh, totalGeneratedKwh, isVip }: Props) {
  return (
    <section>
      <h2>Энергия</h2>
      <p>Доступно: {availableKwh.toFixed(8)} kWh</p>
      <p>Всего сгенерировано: {totalGeneratedKwh.toFixed(8)} kWh</p>
      <p>VIP: {isVip ? "Да" : "Нет"}</p>
    </section>
  );
}

export default EnergyGauge;

// ======================================================================
// Пояснения «для чайника»:
//   • Виджет только отображает числа, не меняя их.
//   • Формат с 8 знаками подчёркивает точность Decimal(8) канона.
//   • VIP берётся из API: на клиенте статус не вычисляется.
//   • Обратной конверсии EFHC→kWh здесь нет и быть не может.
// ======================================================================
