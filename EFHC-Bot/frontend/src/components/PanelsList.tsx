/**
======================================================================
== EFHC Bot WebApp — список панелей                                   ==
======================================================================
Назначение: показывает активные панели пользователя и срок их жизни без изменений балансов.
Инварианты: цена панели (100 EFHC) и срок жизни (180 дней) фиксированы на бэкенде; клиент только визуализирует.
ИИ-защита: выводит пустой список безопасно, не инициирует покупок — деньги движутся только через API с Idempotency-Key.
Запреты: нет прямого дебета/кредита, нет расчёта генерации на клиенте.
======================================================================
*/
type PanelCard = {
  id: string;
  expiresAt: string;
  dailyKwh: number;
};

type Props = {
  panels: PanelCard[];
};

function PanelsList({ panels }: Props) {
  if (!panels.length) {
    return <p>Панели отсутствуют.</p>;
  }

  return (
    <section>
      <h2>Мои панели</h2>
      <ul>
        {panels.map((panel) => (
          <li key={panel.id}>
            <div>#{panel.id}</div>
            <div>Срок до: {panel.expiresAt}</div>
            <div>Начисление в день: {panel.dailyKwh.toFixed(8)} kWh</div>
          </li>
        ))}
      </ul>
    </section>
  );
}

export default PanelsList;

// ======================================================================
// Пояснения «для чайника»:
//   • Список только читает данные панелей, не создаёт новые.
//   • Стоимость панели и срок жизни заданы на сервере (100 EFHC, 180 дней).
//   • При пустом списке UI просто сообщает об отсутствии панелей.
// ======================================================================
