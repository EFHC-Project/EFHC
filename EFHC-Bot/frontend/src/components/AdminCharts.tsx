/**
======================================================================
== EFHC Bot WebApp — админские графики                                ==
======================================================================
Назначение: визуализирует показатели (например, лаги догонов, дефицит банка) без изменений данных.
Инварианты: значения поступают из серверных отчётов; клиент их не вычисляет и не правит.
ИИ-защита: безопасный рендер простых баров/чисел без внешних библиотек, устойчив к пустым данным.
Запреты: не запускает админ-действия и не проводит деньги.
======================================================================
*/
type AdminMetric = {
  label: string;
  value: number;
};

type Props = {
  metrics: AdminMetric[];
};

function AdminCharts({ metrics }: Props) {
  if (!metrics.length) {
    return <p>Отчёты пока недоступны.</p>;
  }

  return (
    <section>
      <h2>Отчёты</h2>
      <ul>
        {metrics.map((metric) => (
          <li key={metric.label}>
            {metric.label}: {metric.value}
          </li>
        ))}
      </ul>
    </section>
  );
}

export default AdminCharts;

// ======================================================================
// Пояснения «для чайника»:
//   • Графики здесь сведены к списку, чтобы не тянуть лишние зависимости.
//   • Числа приходят из бэкенда (например, reports_service), клиент их не меняет.
//   • Денежных операций нет, только визуализация.
// ======================================================================
