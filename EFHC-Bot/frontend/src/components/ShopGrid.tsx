/**
======================================================================
== EFHC Bot WebApp — витрина магазина                                 ==
======================================================================
Назначение: отображает пакеты EFHC и статус активности без проведения оплат на клиенте.
Инварианты: цена 0 означает деактивацию; оплата и Idempotency-Key обрабатываются на бэкенде и TON watcher.
ИИ-защита: список безопасно показывает только доступные карточки, без выполнения транзакций.
Запреты: нет автодоставки NFT, нет локальных расчётов баланса.
======================================================================
*/
type ShopItem = {
  id: string;
  title: string;
  priceEfhc: number;
  active: boolean;
};

type Props = {
  items: ShopItem[];
};

function ShopGrid({ items }: Props) {
  if (!items.length) {
    return <p>Каталог временно пуст.</p>;
  }

  return (
    <section>
      <h2>Магазин</h2>
      <div>
        {items.map((item) => (
          <article key={item.id}>
            <h3>{item.title}</h3>
            <p>Стоимость: {item.priceEfhc} EFHC</p>
            <p>Статус: {item.active ? "активен" : "неактивен"}</p>
          </article>
        ))}
      </div>
    </section>
  );
}

export default ShopGrid;

// ======================================================================
// Пояснения «для чайника»:
//   • Карточка с ценой 0 считается выключенной и только отображается.
//   • Покупка фактически происходит на сервере и через TON/банк EFHC.
//   • Клиент не генерирует Idempotency-Key — его ставит бэкенд/клиентское API.
// ======================================================================
