/**
======================================================================
== EFHC Bot WebApp — админская таблица                                ==
======================================================================
Назначение: отображает метрики/записи админ-раздела без выполнения управленческих действий.
Инварианты: доступ в админку контролирует бэкенд через require_admin_or_nft_or_key; клиент лишь показывает данные.
ИИ-защита: устойчивый вывод при пустых или частично загруженных данных.
Запреты: не изменяет записи, не проводит денежные операции, не обходит авторизацию.
======================================================================
*/
type AdminRow = {
  label: string;
  value: number | string;
};

type Props = {
  rows: AdminRow[];
};

function AdminTable({ rows }: Props) {
  if (!rows.length) {
    return <p>Метрики недоступны.</p>;
  }

  return (
    <section>
      <h2>Админ метрики</h2>
      <table>
        <tbody>
          {rows.map((row) => (
            <tr key={row.label}>
              <td>{row.label}</td>
              <td>{row.value}</td>
            </tr>
          ))}
        </tbody>
      </table>
    </section>
  );
}

export default AdminTable;

