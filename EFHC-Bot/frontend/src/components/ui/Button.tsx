/**
======================================================================
== EFHC Bot WebApp UI — кнопка                                        ==
======================================================================
Назначение: базовая кнопка без побочных эффектов; события передаются родителю.
Инварианты: не выполняет денежных запросов, не генерирует Idempotency-Key.
ИИ-защита: безопасный handler, если не передан — кнопка не ломает рендер.
Запреты: не делает сетевые вызовы сама.
======================================================================
*/
import type { ReactNode } from "react";

type Props = {
  children: ReactNode;
  onClick?: () => void;
  disabled?: boolean;
};

function Button({ children, onClick, disabled = false }: Props) {
  return (
    <button type="button" onClick={onClick} disabled={disabled}>
      {children}
    </button>
  );
}

export default Button;

// ======================================================================
// Пояснения «для чайника»:
//   • Кнопка не ходит в сеть и не трогает деньги — только вызывает onClick.
//   • По умолчанию доступна; disabled блокирует клики безопасно.
//   • Используйте в UI для навигации, не для банковских операций напрямую.
// ======================================================================
