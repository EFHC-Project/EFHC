/**
======================================================================
== EFHC Bot WebApp UI — бейдж                                         ==
======================================================================
Назначение: компактный индикатор статуса (например, VIP/неактивен) без бизнес-логики.
Инварианты: отображает текст как есть; не изменяет данные.
ИИ-защита: без побочных эффектов, безопасен при пустой строке.
Запреты: не содержит ссылок на денежные операции.
======================================================================
*/
type Props = {
  text: string;
};

function Badge({ text }: Props) {
  return <span>{text}</span>;
}

export default Badge;

