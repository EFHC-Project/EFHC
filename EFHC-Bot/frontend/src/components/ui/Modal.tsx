/**
======================================================================
== EFHC Bot WebApp UI — модальное окно                                ==
======================================================================
Назначение: лёгкий модальный контейнер для подтверждений без прямых денежных действий.
Инварианты: управляется родителем через флаг open; не выполняет сетевых запросов.
ИИ-защита: безопасно скрывается при закрытии, не блокирует основной UI.
Запреты: не содержит логики списаний/начислений EFHC.
======================================================================
*/
import type { ReactNode } from "react";

type Props = {
  open: boolean;
  children: ReactNode;
  onClose?: () => void;
};

function Modal({ open, children, onClose }: Props) {
  if (!open) {
    return null;
  }

  return (
    <div role="dialog" aria-modal="true">
      <div>{children}</div>
      <button type="button" onClick={onClose}>
        Закрыть
      </button>
    </div>
  );
}

export default Modal;

// ======================================================================
// Пояснения «для чайника»:
//   • Модалка показывает переданные children, закрытие управляется родителем.
//   • Денежные операции должны выполняться вне модалки и только через API с Idempotency-Key.
//   • Если open=false, компонент не рендерится и не тратит ресурсы.
// ======================================================================
