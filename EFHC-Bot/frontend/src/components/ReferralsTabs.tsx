/**
======================================================================
== EFHC Bot WebApp — витрина рефералов                                ==
======================================================================
Назначение: отображает активных и неактивных рефералов без изменения их статуса.
Инварианты: активность определяется покупкой панели на сервере; клиент не переключает состояния.
ИИ-защита: безопасное разделение вкладок, устойчивость к пустым спискам.
Запреты: нет начислений бонусов на клиенте, нет P2P переводов.
======================================================================
*/
type Referral = {
  username: string;
  isActive: boolean;
};

type Props = {
  referrals: Referral[];
};

function ReferralsTabs({ referrals }: Props) {
  const active = referrals.filter((item) => item.isActive);
  const inactive = referrals.filter((item) => !item.isActive);

  return (
    <section>
      <h2>Рефералы</h2>
      <div>
        <h3>Активные</h3>
        {active.length ? (
          <ul>
            {active.map((referral) => (
              <li key={referral.username}>{referral.username}</li>
            ))}
          </ul>
        ) : (
          <p>Активных пока нет.</p>
        )}
      </div>
      <div>
        <h3>Неактивные</h3>
        {inactive.length ? (
          <ul>
            {inactive.map((referral) => (
              <li key={referral.username}>{referral.username}</li>
            ))}
          </ul>
        ) : (
          <p>Неактивных пока нет.</p>
        )}
      </div>
    </section>
  );
}

export default ReferralsTabs;

// ======================================================================
// Пояснения «для чайника»:
//   • Активность реферала определяется сервером после первой покупки панели.
//   • Клиент только показывает списки; бонусы начисляются банком EFHC.
//   • Пустые списки безопасно отображаются без ошибок.
// ======================================================================
